#include "libft.h"

void	ft_striteri(const char *s, void (*f)(unsigned int, char))
{
	unsigned int	i;

	i = 0;
	if (!s || !f)
		return;
	while (s)
	{
		f(i, *s);
		s++;
		i++;
	}
}
